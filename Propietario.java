package ClinicaVeterinaria1;

public class Propietario {

	    private String nombre;
	    private String telefono;

	    public Propietario(String nombre, String telefono) {
	        this.nombre = nombre;
	        this.telefono = telefono;
	    }

	    public String getNombre() {
	        return nombre;
	    }
	
	
	
}
